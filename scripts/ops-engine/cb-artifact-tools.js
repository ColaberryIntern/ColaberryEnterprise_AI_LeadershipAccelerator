  // too. Easiest: read the token from the same fallback the dispatcher uses.
  // Caller passes us the Authorization header indirectly through bcGet/bcPost
  // (they already have the token). We extract by calling a sentinel GET first.
  const ping = await fetch(`${BASE}/projects/${bucketId}.json`, { headers: { Authorization: tokenHeader || '', Accept: 'application/json' } });
