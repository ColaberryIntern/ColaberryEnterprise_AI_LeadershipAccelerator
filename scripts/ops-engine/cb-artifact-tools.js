// Artifact generation tools for CB System: PDF, XLSX, AI image.
//
// Each function:
//   - Generates the artifact in /tmp/
//   - Uploads to Basecamp as an attachment_sgid via /attachments.json
//   - Posts a comment on the requesting thread with the file linked
//
// Used by the @CB LLM handler when team members ask CB to "give me a PDF of X"
// or "build a spreadsheet for Y" or "make me an image of Z".

const fs = require('fs');
const path = require('path');

const REPO = path.resolve(__dirname, '../..');

function mimeFor(filename) {
  const ext = (filename.match(/\.([a-z0-9]+)$/i) || [])[1]?.toLowerCase() || '';
  return ({
    pdf: 'application/pdf',
    xlsx: 'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet',
    csv: 'text/csv',
    txt: 'text/plain',
    png: 'image/png', jpg: 'image/jpeg', jpeg: 'image/jpeg',
  }[ext] || 'application/octet-stream');
}

// =============================================================================
// PDF generation via pdfkit
// =============================================================================
async function buildPdf({ title, sections, filename }) {
  const PDFDocument = require(path.resolve(REPO, 'node_modules/pdfkit'));
  const outPath = path.join('/tmp', filename || `cb-pdf-${Date.now()}.pdf`);
  return new Promise((resolve, reject) => {
    try {
      const doc = new PDFDocument({ size: 'LETTER', margin: 56 });
      const stream = fs.createWriteStream(outPath);
      doc.pipe(stream);

      // Title
      doc.fontSize(20).fillColor('#1a365d').font('Helvetica-Bold').text(title || 'CB System Document', { align: 'left' });
      doc.moveDown(0.5);
      doc.fontSize(9).fillColor('#64748b').font('Helvetica').text(`Generated by CB System on ${new Date().toISOString().slice(0, 10)}`);
      doc.moveDown(1);

      // Sections
      for (const sec of (sections || [])) {
        if (sec.heading) {
          doc.fontSize(14).fillColor('#1a365d').font('Helvetica-Bold').text(sec.heading);
          doc.moveDown(0.3);
        }
        if (sec.body) {
          doc.fontSize(11).fillColor('#1f2937').font('Helvetica').text(sec.body, { align: 'left' });
          doc.moveDown(0.5);
        }
        if (Array.isArray(sec.bullets) && sec.bullets.length) {
          for (const b of sec.bullets) {
            doc.fontSize(11).fillColor('#1f2937').font('Helvetica').text(`  - ${b}`);
          }
          doc.moveDown(0.5);
        }
        if (sec.table && Array.isArray(sec.table.rows) && sec.table.rows.length) {
          const headers = sec.table.headers || [];
          const colCount = Math.max(headers.length, sec.table.rows[0].length);
          const colWidth = (500 - 0) / colCount;
          if (headers.length) {
            doc.font('Helvetica-Bold').fontSize(10).fillColor('#1a365d');
            headers.forEach((h, i) => doc.text(h, 56 + i * colWidth, doc.y, { width: colWidth - 8, continued: i < headers.length - 1 }));
            doc.moveDown(0.4);
          }
          doc.font('Helvetica').fontSize(10).fillColor('#1f2937');
          for (const row of sec.table.rows) {
            row.forEach((cell, i) => doc.text(String(cell), 56 + i * colWidth, doc.y, { width: colWidth - 8, continued: i < row.length - 1 }));
            doc.moveDown(0.3);
          }
          doc.moveDown(0.5);
        }
      }
      doc.end();
      stream.on('finish', () => resolve(outPath));
      stream.on('error', reject);
    } catch (e) { reject(e); }
  });
}

// =============================================================================
// XLSX generation via ExcelJS
// =============================================================================
async function buildXlsx({ title, sheets, filename }) {
  const ExcelJS = require(path.resolve(REPO, 'node_modules/exceljs'));
  const wb = new ExcelJS.Workbook();
  wb.creator = 'CB System';
  wb.created = new Date();
  for (const s of (sheets || [])) {
    const ws = wb.addWorksheet(s.name?.slice(0, 30) || 'Sheet');
    if (Array.isArray(s.headers) && s.headers.length) {
      ws.addRow(s.headers);
      const headerRow = ws.getRow(1);
      headerRow.font = { bold: true, color: { argb: 'FFFFFFFF' } };
      headerRow.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FF1A365D' } };
      headerRow.alignment = { vertical: 'middle', horizontal: 'left' };
    }
    for (const row of (s.rows || [])) {
      ws.addRow(row);
    }
    ws.columns.forEach((col) => {
      let maxLen = 10;
      col.eachCell({ includeEmpty: false }, (cell) => {
        const len = (cell.value || '').toString().length;
        if (len > maxLen) maxLen = len;
      });
      col.width = Math.min(maxLen + 2, 50);
    });
  }
  const outPath = path.join('/tmp', filename || `cb-xlsx-${Date.now()}.xlsx`);
  await wb.xlsx.writeFile(outPath);
  return outPath;
}

// =============================================================================
// Image generation via OpenAI gpt-image-1 (DALL-E 3 successor)
// =============================================================================
async function buildImage({ prompt, size = '1024x1024', filename }) {
  if (!process.env.OPENAI_API_KEY) throw new Error('OPENAI_API_KEY required');
  const OpenAI = require(path.resolve(REPO, 'node_modules/openai')).default;
  const openai = new OpenAI({ apiKey: process.env.OPENAI_API_KEY });
  const r = await openai.images.generate({
    model: 'gpt-image-1',
    prompt,
    size,
    n: 1,
  });
  const b64 = r.data?.[0]?.b64_json;
  if (!b64) throw new Error('OpenAI returned no image data');
  const outPath = path.join('/tmp', filename || `cb-image-${Date.now()}.png`);
  fs.writeFileSync(outPath, Buffer.from(b64, 'base64'));
  return outPath;
}

